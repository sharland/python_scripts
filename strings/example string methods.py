#Quotation manipulation
#Demonstrates string methods

#famous quote
quote = "Never trust a computer you can't throw out the window"

print("Original quote:")
print(quote)

print("\nIn uppercase:")
print(quote.upper())

print("\nIn lowercase:")
print(quote.lower())

print("\nAs a title:")
print(quote.title())

print("\nWith a minor adjustment")
print(quote.replace("window", "bath"))

print("\nOriginal quote again")
print(quote)


